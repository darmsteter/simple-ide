import java.awt.Color
import Colors.BLUE

enum class KotlinKeywords(val value: String, val color: Color) {
    FUN("fun", BLUE.color),
    VAL("val", BLUE.color),
    VAR("var", BLUE.color),
    CLASS("class", BLUE.color),
    FOR("for", BLUE.color),
    RETURN("return", BLUE.color),
    IF("if", BLUE.color),
    IN("in", BLUE.color),
    TRUE("true", BLUE.color),
    FALSE("false", BLUE.color),
    OVERRIDE("override", BLUE.color),
}